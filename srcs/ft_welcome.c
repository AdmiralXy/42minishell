void	ft_print(char *str)
{
	char		*result;
	char		*temp;
	static int	i;

	if (!i)
		i = 0;
	i++;
	temp = ft_strjoin("\001\033[1;91m\002", str);
	result = ft_strjoin(temp, "\001\033[0m\002");
	printf("%s", result);
	free(temp);
	free(result);
	if (i % 3 == 0)
		printf("\n");
}

